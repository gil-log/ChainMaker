package kr.happyjob.chainmaker.sti.model;

public class InquiryModel {
	
	private int row_num;
	private String inq_no;
	private String inq_title;
	private String inq_content;
	private String inq_regdate;
	private String inq_moddate;
	private String category_cd;
	private String answer_cd;
	private String loginID;
	private String inq_antitle;
	
	
	public int getRow_num() {
		return row_num;
	}
	public void setRow_num(int row_num) {
		this.row_num = row_num;
	}
	public String getInq_no() {
		return inq_no;
	}
	public void setInq_no(String inq_no) {
		this.inq_no = inq_no;
	}
	public String getInq_title() {
		return inq_title;
	}
	public void setInq_title(String inq_title) {
		this.inq_title = inq_title;
	}
	public String getInq_content() {
		return inq_content;
	}
	public void setInq_content(String inq_content) {
		this.inq_content = inq_content;
	}
	public String getInq_regdate() {
		return inq_regdate;
	}
	public void setInq_regdate(String inq_regdate) {
		this.inq_regdate = inq_regdate;
	}
	public String getInq_moddate() {
		return inq_moddate;
	}
	public void setInq_moddate(String inq_moddate) {
		this.inq_moddate = inq_moddate;
	}
	public String getCategory_cd() {
		return category_cd;
	}
	public void setCategory_cd(String category_cd) {
		this.category_cd = category_cd;
	}
	public String getAnswer_cd() {
		return answer_cd;
	}
	public void setAnswer_cd(String answer_cd) {
		this.answer_cd = answer_cd;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
	public String getInq_antitle() {
		return inq_antitle;
	}
	public void setInq_antitle(String inq_antitle) {
		this.inq_antitle = inq_antitle;
	}

	
	
}
