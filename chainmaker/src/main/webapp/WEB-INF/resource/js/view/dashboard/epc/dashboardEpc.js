

$(function() {

	initSlideShow();
	
});

function initSlideShow(){
	$('.post-wrapper').slick({
		  slidesToShow: 3,
		  slidesToScroll: 1,
		  autoplay: true,
		  autoplaySpeed: 500,
		  prevArrow : "<button type='button' class='slick-prev'>Previous</button>",
		nextArrow : "<button type='button' class='slick-next'>Next</button>",
		});
}